public class Hypotenuse {
    public static void main(final String[] args) {
        float sideA = 3.0f;
        float sideB = 4.0f;
        double hypotenuse = Math.sqrt(sideA * sideA + sideB * sideB);

        System.out.println("Hypotenuse is " + hypotenuse);
    }
}
